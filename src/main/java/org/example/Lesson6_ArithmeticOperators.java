/*
5 types of arithmetic operators
1. Addition (+)
2. Subtraction (-)
3. Multiplication (*)
4. Division (/)
5. Modulus (%)
 */
public class Lesson6_ArithmeticOperators {
    public static void main(String[] args) {
        int x = 1;
        double y = 2;
        double z = x + y; // int / int = int (or) int / double = double

        System.out.println(z);
    }
}
