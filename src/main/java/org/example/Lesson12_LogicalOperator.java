package org.example;
/*
There exists 3 types of logical operators
1) AND (&&)
    - true && true = true
    - true && false = false
    - false && true = false
    - false && false = false
2) OR (||)
    - true || true = true
    - true || false = true
    - false || true = true
    - false || false = false
3) NOT (!)
 */
public class Lesson12_LogicalOperator {
    public static void main(String[] args) {

        System.out.println(true && false && true && true);

    }
}
