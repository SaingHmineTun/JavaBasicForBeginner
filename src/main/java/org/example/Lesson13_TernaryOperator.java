package org.example;
/*
Work like assignment operators
Syntax - boolean ? trueValue : falseValue
 */
public class Lesson13_TernaryOperator {
    public static void main(String[] args) {
        boolean isMale = true;
        String name = isMale ? "Mr" : "Mrs";
        System.out.println(name);
    }
}

